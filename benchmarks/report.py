"""Generate a markdown comparison report from MLflow runs.

Compares multiple (skill, platform) combinations side by side.
Useful for cross-platform benchmarking: Claude Code vs OpenCode vs pi-agent.

Usage:
  uv run python report.py --skill plan-it
  uv run python report.py --skill plan-it --platforms api,claude-code,opencode
  uv run python report.py --skill implement-it --out results/implement-it-report.md
"""
from __future__ import annotations

import sys
from pathlib import Path

import click
import mlflow

_MLRUNS_DIR = str(Path(__file__).parent / "mlruns")

_REPORT_METRICS = [
    ("accuracy", "Accuracy", ".3f"),
    ("precision", "Precision", ".3f"),
    ("recall", "Recall", ".3f"),
    ("f1", "F1", ".3f"),
    ("quality_score", "Quality Score", ".2f"),
    ("test_pass_rate", "Test Pass Rate", ".3f"),
    ("judge_score", "Judge Score", ".2f"),
    ("latency_ms", "Latency p50 (ms)", ".0f"),
    ("total_tokens", "Total Tokens", ".0f"),
]


def _fetch_summary_metrics(skill: str, platform: str) -> dict[str, dict[str, float]]:
    client = mlflow.tracking.MlflowClient()
    exp = client.get_experiment_by_name(f"{skill}__{platform}")
    if not exp:
        return {}
    runs = client.search_runs(
        experiment_ids=[exp.experiment_id],
        filter_string="tags.run_type = 'summary'",
        order_by=["start_time DESC"],
        max_results=1,
    )
    if not runs:
        return {}
    metrics = runs[0].data.metrics
    result: dict[str, dict[str, float]] = {"with_skill": {}, "without_skill": {}}
    for key, _ in [(m, _) for m, _, _ in _REPORT_METRICS]:
        result["with_skill"][key] = metrics.get(f"with_skill__{key}__mean", 0.0)
        result["without_skill"][key] = metrics.get(f"without_skill__{key}__mean", 0.0)
    return result


def _render_markdown(
    skill: str,
    platforms: list[str],
    data: dict[str, dict[str, dict[str, float]]],
) -> str:
    lines = [f"# Benchmark Report: `{skill}`\n"]
    lines.append(f"Generated by `report.py` from MLflow runs in `benchmarks/mlruns`.\n")

    for metric_key, metric_label, fmt in _REPORT_METRICS:
        lines.append(f"\n## {metric_label}\n")
        header = "| Platform | With Skill | Without Skill | Δ |"
        separator = "|---|---|---|---|"
        lines.append(header)
        lines.append(separator)
        for platform in platforms:
            d = data.get(platform, {})
            w = d.get("with_skill", {}).get(metric_key, None)
            wo = d.get("without_skill", {}).get(metric_key, None)
            if w is None or wo is None:
                lines.append(f"| {platform} | — | — | — |")
                continue
            delta = w - wo
            sign = "+" if delta >= 0 else ""
            lines.append(
                f"| {platform} | {w:{fmt}} | {wo:{fmt}} | {sign}{delta:{fmt}} |"
            )

    lines.append("\n---\n")
    lines.append("*Run `uv run python corpus_qc.py` to verify corpus quality before trusting these results.*\n")
    lines.append("*Run `mlflow ui --backend-store-uri file://./benchmarks/mlruns` for the interactive dashboard.*\n")
    return "\n".join(lines)


@click.command()
@click.option("--skill", required=True, help="Skill name: plan-it, implement-it")
@click.option("--platforms", default="api", help="Comma-separated platform list")
@click.option("--out", default=None, help="Output file path (default: print to stdout)")
@click.option("--tracking-uri", default=None)
def main(skill: str, platforms: str, out: str | None, tracking_uri: str | None):
    """Generate markdown comparison report from MLflow summary runs."""
    uri = tracking_uri or f"file://{_MLRUNS_DIR}"
    mlflow.set_tracking_uri(uri)

    platform_list = [p.strip() for p in platforms.split(",")]
    data: dict[str, dict[str, dict[str, float]]] = {}
    missing = []

    for platform in platform_list:
        d = _fetch_summary_metrics(skill, platform)
        if d:
            data[platform] = d
        else:
            missing.append(platform)

    if missing:
        click.echo(f"Warning: no summary runs found for: {', '.join(missing)}", err=True)

    if not data:
        click.echo("No data available. Run experiments first with run.py.", err=True)
        sys.exit(1)

    report = _render_markdown(skill, platform_list, data)

    if out:
        Path(out).parent.mkdir(parents=True, exist_ok=True)
        Path(out).write_text(report)
        click.echo(f"Report written to {out}")
    else:
        click.echo(report)


if __name__ == "__main__":
    main()
